function sanitizeTitle(title) {
  return title
    .replace(/[？?]/g, "")
    .replace(/[“”"：:，,。！!、；;（）()]/g, "")
    .replace(/\s+/g, "")
    .replace(/[^\u4e00-\u9fa5A-Za-z0-9_]+/g, "_")
    .replace(/_+/g, "_")
    .slice(0, 86);
}

function article([num, title, concept, pain, need, action, product]) {
  return `# 问题${num}：${title}

你有没有发现，真正让人疲惫的不是没有方法，而是每一种方法都像在要求你先变成另一个人。

今天这个问题可以叫作：${concept}。它看起来像一个内容选题，实际上是一条工作流线索。因为它背后不是抽象观点，而是一个真实痛点：${pain}。

如果这篇只写成共鸣，它会让人点头；但如果写成系统，它就能变成模板、栏目、产品入口和评论回流。

## 01 收敛

一句话打穿：${need}。

这不是把问题说得更漂亮，而是把它放到能执行的位置上。ADHD x AI 内容最重要的能力，不是不断生产新标题，而是把一个痛点压缩成可以被读者使用的动作。

所以这篇的核心不是“我又发现了一个现象”，而是：这个现象怎样进入系统，怎样帮助读者少一点自责、多一个入口、多一次真实反馈。

## 02 穿透

${concept}至少有三层。

第一层，是读者的当下感受。${pain}。如果这里写得不够具体，读者只会觉得你在讲道理，不会觉得你在讲他。

第二层，是背后的机制。它通常不是意志力问题，而是执行功能、注意力分配、反馈回路、环境匹配或认知负荷出了问题。ADHD 用户最需要的不是再被要求努力，而是知道自己到底是哪一个接口掉线。

第三层，是 AI/Agent 可以接手的动作。AI 不应该只负责写得更顺，而应该负责倾倒、整理、排序、降级、提醒、回放、记录和回流。只有这样，内容才会从表达变成外部执行系统。

## 03 铁证

生活场景证据：当读者遇到这个问题时，他往往不是没有想法，而是被太多隐形步骤卡住。一个看似简单的动作，背后可能藏着启动、排序、情绪、时间和完成感五个断点。

个人观察证据：很多高发散的人并不是缺资产，而是缺资产化协议。他们有聊天、有草稿、有工具试错、有失败经验、有读者语言，但如果没有系统接住，这些东西很快会散掉。

反事实证据：如果把这个选题只当观点，发布后最多收获几句共鸣。可如果把它做成 ${product}，再配一个具体评论问题，它就会开始产生需求、样本和下一篇内容。

## 04 决策

你要把这个题从“可以写”升级成“可以验证”。

写作时不要停在解释，而要固定追问：

- 读者具体卡在哪里？
- 哪个认知功能在掉线？
- AI 可以接手哪一步？
- 最小动作是什么？
- 评论区要采集什么需求？

这五问能把内容从漂亮观点拉回现实。

## 05 落地

今天的落地动作：${action}。

格式很简单：

- 当前痛点：
- 掉线接口：
- AI 可接手动作：
- 输出文件或模板：
- 评论区验证问题：

只要这五行写出来，这个选题就不再只是灵感，而会进入发布、回流和产品化。

## 发布接口

- 公众号版核心标题：${title}
- 小红书版钩子：${pain}
- 知乎回答问题：为什么 ${concept} 对 ADHD x AI 内容很关键？
- 短视频前三秒：这不是又一个观点，这是一个可以落地的入口。
- 评论区问题：你在这个问题上最想要模板、清单、陪跑，还是一次具体拆解？
`;
}

let created = 0;
for (const topic of topics) {
  const [num, title] = topic;
  const prefix = `问题${num}_`;
  const exists = fs.readdirSync(root).some((name) => name.startsWith(prefix) && name.endsWith(".md"));
  if (exists) continue;
  const fileName = `${prefix}${sanitizeTitle(title)}.md`;
  fs.writeFileSync(path.join(root, fileName), article(topic), "utf8");
  created++;
}

